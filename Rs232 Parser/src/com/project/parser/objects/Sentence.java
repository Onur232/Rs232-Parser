package com.sur.parser.objects;

public class Sentence {
	
	private int surat;
	private int umk;
	private int rota;
	
	
	public Sentence() {
	}


	public int getSurat() {
		return surat;
	}


	public void setSurat(int surat) {
		this.surat = surat;
	}


	public int getUmk() {
		return umk;
	}


	public void setUmk(int umk) {
		this.umk = umk;
	}


	public int getRota() {
		return rota;
	}


	public void setRota(int rota) {
		this.rota = rota;
	}


		
}
